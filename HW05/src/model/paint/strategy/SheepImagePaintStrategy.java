package model.paint.strategy;


import java.awt.geom.AffineTransform;

import model.painting.ImagePaintStrategy;

/** paint strategy that paints a running sheep image */
public class SheepImagePaintStrategy extends ImagePaintStrategy{

	/**
	 * The path of image file
	 */
	private static final String filename = "images/sheep_animate.gif";
	
	/**
	 * The affine transform object
	 */
	private static AffineTransform at = new AffineTransform();
	
	/**
	 * The fill factor
	 */
	private static double fillFactor = 1.0;
	
	/**
	 * Constructor that loads the sheep image
	 */
	public SheepImagePaintStrategy() {
		super(at, filename, fillFactor);
	}
} 
